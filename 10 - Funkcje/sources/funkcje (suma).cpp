
    #include <iostream>
    using namespace std;

    void suma(int a, int b)
    {
        int c;
        c = a + b;
        cout<<a<<" + "<<b<<" = "<<c<<endl;
    }

    int main()
    {
        int a=3, b=5;

        suma(a,b);

        int x=23, y=45;

        suma(x,y);

        suma(4,11);

        return 0;

    }


